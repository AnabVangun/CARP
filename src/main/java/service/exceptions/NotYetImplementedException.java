package service.exceptions;

/**
 * Exception to signal that the method has not yet been implemented.
 * @author TLM
 */
public class NotYetImplementedException extends RuntimeException {
	private static final long serialVersionUID = -5926452450305479861L;

	/**
	 * Creates a new {@link NotYetImplementedException}.
	 */
	public NotYetImplementedException() {
		super();
	}
}
